package de.pgalise.simulation.shared.city;

import java.io.Serializable;

/**
 * A bus stop is a {@link Node} with a bus stop name.
 * @author Timo
 */
public class BusStop extends Node implements Serializable {
	private static final long serialVersionUID = -8112215320374809034L;
	private String name;

	/**
	 * New bus stop.
	 * 
	 * @param name
	 * @param latitude
	 * @param longitude
	 */
	public BusStop(String id, String name, double latitude, double longitude) {
		super(id, latitude, longitude);
		this.name = name;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (!super.equals(obj)) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		BusStop other = (BusStop) obj;
		if (name == null) {
			if (other.name != null) {
				return false;
			}
		} else if (!name.equals(other.name)) {
			return false;
		}
		return true;
	}
}
